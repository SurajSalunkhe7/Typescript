function Itration_For():void
{
    var i:number=0;
    for(i=1;i<=5;i++)
    {
        console.log("Hello");
    }
}

function Itration_while():void
{
    var i:number=1;
    while(i<=5)
    {
        console.log("Hello");
        i++;  
    }

}
